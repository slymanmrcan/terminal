export function resolveLocale(value) {
  if (!value) return null;
  const normalized = String(value).toLowerCase().trim();
  return SUPPORTED_LOCALES.includes(normalized) ? normalized : null;
}

export function getInitialLocale(defaultLocale = "tr") {
  const browserLocale = (navigator.language || "").toLowerCase().startsWith("tr") ? "tr" : "en";
  try {
    const stored = localStorage.getItem(LOCALE_STORAGE_KEY);
    const resolved = resolveLocale(stored);
    if (resolved) return resolved;
  } catch (error) {
    // ignore storage errors
  }
  return resolveLocale(defaultLocale) || browserLocale;
}

export function persistLocale(locale) {
  const resolved = resolveLocale(locale);
  if (!resolved) return null;
  try {
    localStorage.setItem(LOCALE_STORAGE_KEY, resolved);
  } catch (error) {
    // ignore storage errors
  }
  document.documentElement.lang = resolved;
  return resolved;
}
